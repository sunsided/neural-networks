namespace Neural.Perceptron
{
    /// <summary>
    /// Enum LayerType
    /// </summary>
    public enum LayerType
    {
        /// <summary>
        /// The layer is the input layer, i.e.
        /// the first one in the network.
        /// </summary>
        Input,

        /// <summary>
        /// The layer is a hidden layer.
        /// </summary>
        Hidden,

        /// <summary>
        /// The layer is the output layer, i.e.
        /// the last one in the network.
        /// </summary>
        Output
    }
}
